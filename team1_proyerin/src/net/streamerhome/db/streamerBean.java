package net.streamerhome.db;

public class streamerBean {
	private String email;
	private int music_cnt;
	private int com_cnt;
	private int point;
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public int getMusic_cnt() {
		return music_cnt;
	}
	public void setMusic_cnt(int music_cnt) {
		this.music_cnt = music_cnt;
	}
	public int getCom_cnt() {
		return com_cnt;
	}
	public void setCom_cnt(int com_cnt) {
		this.com_cnt = com_cnt;
	}
	public int getPoint() {
		return point;
	}
	public void setPoint(int point) {
		this.point = point;
	}
	
	
}
